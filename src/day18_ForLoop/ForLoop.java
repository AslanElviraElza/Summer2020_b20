package day18_ForLoop;

public class ForLoop {
    public static void main(String[] args) {


        for(int i=1; i<=5; i++){
            System.out.println("Cybertek Batch 20");
        }

        for(int i=1; i<=10; i++){
            System.out.println(i);
        }

        System.out.println("============================");

        for(int i=10; i>=1; i--){
            System.out.println(i);

        }

    }
}

