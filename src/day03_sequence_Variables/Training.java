package day03_sequence_Variables;

public class Training {


    public static void main(String[] args) {
        for (int j = 0; j < 8; j++) {
            System.out.print(" * ");
        }
        System.out.println();

        for (int i = 0; i < 6; i++) {
            System.out.print("* ");
            for (int z = 0; z < 20; z++) {
                System.out.print(" ");
            }

            System.out.println("* ");
        }


        for (int k = 0; k < 8; k++) {
            System.out.print(" * ");
        }


    }

}

