package Day12Switch_Scanner;

public class Switch_Scanner {
    public static void main(String[] args) {
           String productName = "Iphone";

           switch (productName){
               case "galaxy":
                   System.out.println("Samsung");
                   break;
               case "iphone":
                   System.out.println("APPLE");
                   break;
               case "Macbook" :
                   System.out.println("APPLE");
                   break;

           }
        System.out.println(productName);

    }
}
