package day0.SadecePractice;

public class ArrayChar {
    public static void main(String[] args) {

        char a[] = new char[5];

        int i;

        for (i = 0; i < 5; i++){
            a[i] = 'a';
            System.out.println(a[i]);
        }


        int b[] = new int[5];

        for (i = 0; i < 5; i++){
            b[i] = 'a';
            System.out.println(b[i]);
        }

        String c[] = new String[5];

        for (i = 0; i < 5; i++){
            c[i] = "a";
            System.out.println(c[i]);
        }



    }
}
